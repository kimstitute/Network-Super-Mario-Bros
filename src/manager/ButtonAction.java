package manager;

/**
 * 게임 내에서 발생하는 모든 입력 액션을 정의하는 열거형(enum) 클래스
 * 키보드 입력, 마우스 클릭, 게임 상태 변경 등 모든 사용자 인터랙션을 표준화된 형태로 관리
 *
 * @author 네트워크프로그래밍 팀
 * @version 1.0
 * @since 2024-12-14
 */
public enum ButtonAction {
    /**
     * 점프 액션 - 스페이스바 또는 지정된 점프 키를 눌렀을 때 발생
     * 마리오가 바닥에 닿아있을 때만 점프 가능하며, 중력 시스템과 연동됨
     */
    JUMP,
    
    /**
     * 오른쪽 이동 액션 - 오른쪽 방향키를 눌렀을 때 발생
     * 마리오가 오른쪽으로 이동하며, 카메라 위치와 연동되어 화면 스크롤 처리
     */
    M_RIGHT,
    
    /**
     * 왼쪽 이동 액션 - 왼쪽 방향키를 눌렀을 때 발생
     * 마리오가 왼쪽으로 이동하며, 카메라 왼쪽 경계를 넘어갈 수 없음
     */
    M_LEFT,
    
    /**
     * 앉기 액션 - 아래쪽 방향키를 눌렀을 때 발생
     * 현재 구현에서는 미사용 상태이나, 확장성을 위해 유지
     */
    CROUCH,
    
    /**
     * 파이어볼 발사 액션 - Z키 또는 지정된 공격키를 눌렀을 때 발생
     * 마리오가 파이어 폼일 때만 파이어볼 발사 가능
     */
    FIRE,
    
    /**
     * 게임 시작 액션 - 시작 화면에서 게임 시작을 선택했을 때 발생
     * 메인 메뉴에서 '1 PLAYER GAME' 선택 시 트리거됨
     */
    START,
    
    /**
     * 일시정지/재개 액션 - ESC키 또는 P키를 눌렀을 때 발생
     * 게임 진행 중 일시정지, 일시정지 상태에서 재개하는 토글 기능
     */
    PAUSE_RESUME,
    
    /**
     * 액션 완료 알림 - 키보드에서 손을 뗐을 때 발생
     * 이동 키를 뗐을 때 수평 속도를 0으로 만드는 데 사용됨
     */
    ACTION_COMPLETED,
    
    /**
     * 선택 확인 액션 - Enter키 또는 마우스 클릭으로 선택을 확인했을 때 발생
     * 메뉴 항목 선택, 맵 선택 등에서 사용됨
     */
    SELECT,
    
    /**
     * 위쪽 이동 액션 - 위쪽 방향키를 눌렀을 때 발생
     * 메뉴에서 위쪽 항목으로 커서 이동할 때 사용됨
     */
    GO_UP,
    
    /**
     * 아래쪽 이동 액션 - 아래쪽 방향키를 눌렀을 때 발생
     * 메뉴에서 아래쪽 항목으로 커서 이동할 때 사용됨
     */
    GO_DOWN,
    
    /**
     * 시작 화면으로 이동 액션 - 게임 오버나 미션 클리어 후 시작 화면으로 돌아갈 때 발생
     * 주로 게임 오버 화면이나 미션 패스 화면에서 사용됨
     */
    GO_TO_START_SCREEN,
    
    /**
     * 무 액션 상태 - 아무런 입력이 없는 상태를 표현
     * 기본값으로 사용되며, 입력이 없을 때의 상태를 나타냄
     */
    NO_ACTION
}

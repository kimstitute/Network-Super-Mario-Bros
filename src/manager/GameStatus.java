package manager;

/**
 * 게임의 현재 상태를 정의하는 열거형(enum) 클래스
 * 게임 엔진이 현재 어떤 상태에 있는지를 명확히 구분하여 상태 전이를 관리
 * 각 상태에 따라 다른 입력 처리와 렌더링 로직이 실행됨
 *
 * @author 네트워크프로그래밍 팀
 * @version 1.0
 * @since 2024-12-14
 */
public enum GameStatus {
    /**
     * 게임 오버 상태 - 마리오의 생명이 모두 소진되거나 시간이 초과되었을 때
     * 게임 오버 화면을 표시하며, 재시작 또는 메인 메뉴로 돌아갈 수 있는 옵션 제공
     */
    GAME_OVER,
    
    /**
     * 일시정지 상태 - 게임 진행 중에 ESC키나 P키를 눌렀을 때
     * 게임 시간은 정지되며, 일시정지 화면을 표시하고 재개를 대기
     */
    PAUSED,
    
    /**
     * 게임 실행 상태 - 실제 게임 플레이가 진행되는 중
     * 60 FPS 게임 루프가 실행되며, 모든 게임 로직과 물리 시뮬레이션이 활성화됨
     */
    RUNNING,
    
    /**
     * 시작 화면 상태 - 게임이 처음 실행되었을 때 표시되는 메인 메뉴
     * '1 PLAYER GAME', 'HOW TO PLAY', 'EXIT' 등의 옵션을 제공
     */
    START_SCREEN,
    
    /**
     * 맵 선택 화면 상태 - 시작 화면에서 게임 시작을 선택했을 때
     * 플레이할 맵을 선택할 수 있는 화면을 표시하며, 여러 맵 중 하나를 선택
     */
    MAP_SELECTION,
    
    /**
     * 도움말 화면 상태 - 게임 조작법이나 기능 설명을 표시
     * 키보드 컨트롤, 아이템 설명, 게임 목표 등의 정보를 제공
     */
    HELP_SCREEN,
    
    /**
     * 미션 통과 상태 - 맵의 끝에 있는 깃발에 도달했을 때
     * 미션 클리어 메시지를 표시하며, 획득한 점수와 함께 다음 단계로 진행
     */
    MISSION_PASSED,
    
    /**
     * 정보 화면 상태 - 게임 제작 정보나 크레딧을 표시
     * 개발자 정보, 버전 정보, 라이선스 등을 표시하는 화면
     */
    ABOUT_SCREEN,
    
    /**
     * 랭킹 화면 상태 - 게임 종료 후 점수와 시간 순위를 표시
     * 점수 순위와 시간 순위를 별도로 표시하며, 상위 10위까지 관리
     */
    RANKING_SCREEN,
    
    /**
     * 서버 대기 화면 상태 - 멀티플레이어 서버가 클라이언트 접속을 대기 중
     * 서버 주소와 대기 중 메시지를 표시
     */
    WAITING_FOR_PLAYERS,
    
    /**
     * 클라이언트 접속 화면 상태 - 서버에 접속하기 위한 정보 입력
     * IP 주소와 플레이어 이름 입력
     */
    CONNECTING_TO_SERVER
}
